import static java.lang.Character.*;

public class Caracteres {
    public static void main(String[] args) {
        System.out.println(toUpperCase('a'));
        System.out.println(isDigit('8'));
        System.out.println(isDigit('*'));
        System.out.println(isLetter('a'));
        System.out.println(isLetter('*'));
        System.out.println(isLowerCase('a'));
        System.out.println(isLowerCase('A'));
        System.out.println(isUpperCase('B'));
        System.out.println(isUpperCase('K'));
        System.out.println(isSpaceChar(' '));
        System.out.println(isSpaceChar('*'));
        System.out.println(isWhitespace('\n'));
        System.out.println(isWhitespace('*'));
        System.out.println(toLowerCase('D'));
        System.out.println(toLowerCase('*'));
        System.out.println(toUpperCase('J'));
        System.out.println(toUpperCase('8'));
    }
}
