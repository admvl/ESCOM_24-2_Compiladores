import static java.lang.Math.*;

public class MetodosMat {
    public static void main(String[] args) {
        System.out.println("10 a la potencia 3 = " + pow(10,3));
        System.out.println("El número máxio de los números 23.67 y 14.28 = " + max(23.67, 14.28));
        System.out.println("El valor absoluto de -67 = " + abs(-67));
        System.out.println("floor(65.78) = " + Math.floor(65.78));
        System.out.println("floor(34.23) = " + floor(34.23));
        System.out.println("sin(PI/2) = " + sin(PI/2));
    }
}
